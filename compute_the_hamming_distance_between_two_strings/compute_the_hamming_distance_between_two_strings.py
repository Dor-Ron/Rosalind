def get_hamming_distance(str1, str2):
    ''' returns hamming distance between 2 string type arguments '''
    distance = 0

    if len(str1) != len(str2):
        sm = str1 if len(str1) < len(str2) else str2
        distance += abs(len(str1) - len(str2))
    else:
        sm = str1

    for idx in range(len(sm)):
        if str1[idx] != str2[idx]:
            distance += 1
    return distance

